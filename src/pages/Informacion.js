// --- Componente para el Modal (Ventana Emergente) ---
const InfoModal = ({ data, onClose }) => {
  const [isExiting, setIsExiting] = useState(false);
  
  const handleClose = useCallback(() => {
    setIsExiting(true);
    setTimeout(() => {
        onClose();
    }, 400); // Esperamos que la animaci√≥n de salida termine
  }, [onClose]);

  useEffect(() => {
    if (data) {
      document.body.style.overflow = 'hidden';
      const handleKeyDown = (e) => {
        if (e.key === 'Escape') {
          handleClose();
        }
      };
      document.addEventListener('keydown', handleKeyDown);
      return () => {
        document.body.style.overflow = 'unset';
        document.removeEventListener('keydown', handleKeyDown);
      };
    }
  }, [data, handleClose]);

  if (!data) return null;

  return (
    <div
      className={`modal-overlay ${isExiting ? 'modal-overlay-exit' : ''}`}
      onClick={handleClose}
      role="dialog"
      aria-modal="true"
      aria-labelledby="modal-title"
    >
      <div
        className={`modal-content ${isExiting ? 'modal-content-exit' : ''}`}
        onClick={e => e.stopPropagation()}
      >
        <div className="modal-image-container">
          <img
            src={data.imageUrl}
            alt={data.title}
            className="modal-image"
            loading="lazy"
          />
          <div className="modal-image-overlay"></div>
          <button
            onClick={handleClose}
            className="modal-close-button"
            aria-label="Cerrar modal"
          >
            √ó
          </button>
        </div>
        <div className="modal-body">
          <div className="modal-header">
            <h2 id="modal-title" className="modal-title">{data.title}</h2>
            <div className="modal-decoration"></div>
          </div>
          <div className="modal-text">
            {data.content}
          </div>
          <div className="modal-footer">
            <button
              onClick={handleClose}
              className="modal-action-button"
              autoFocus
            >
              <span className="button-text">Entendido</span>
              <span className="button-icon">‚úì</span>
            </button>
          </div>
        </div>
      </div>
    </div>
  );
};

